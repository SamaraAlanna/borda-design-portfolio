var currentLang = localStorage.getItem("lang") || "pt";

function setLang(lang) {
  currentLang = lang;
  localStorage.setItem("lang", lang);
  applyTranslations();
  updateLangBtn();
}

function applyTranslations() {
  var t = i18n[currentLang];
  document.querySelectorAll("[data-i18n]").forEach(function (el) {
    var key = el.getAttribute("data-i18n");
    if (t[key] !== undefined) {
      if (el.getAttribute("data-i18n-html")) {
        el.innerHTML = t[key];
      } else {
        el.textContent = t[key];
      }
    }
  });
  document.documentElement.lang = currentLang === "pt" ? "pt-BR" : "en";
  var btnCV = document.getElementById('btnCV');
  if (btnCV) {
    if (currentLang === 'en') {
      btnCV.href = 'uploads/CV_Samara_EN.pdf';
      btnCV.setAttribute('download', 'CV_Samara_Alanna_EN.pdf');
    } else {
      btnCV.href = 'uploads/CV_Samara.pdf';
      btnCV.setAttribute('download', 'CV_Samara_Alanna.pdf');
    }
  }
}

function updateLangBtn() {
  var btn = document.getElementById('langToggle');
  if (!btn) return;
  var isPt = currentLang === 'pt';
  btn.querySelector('.flag-br').style.opacity = isPt ? '1' : '0.35';
  btn.querySelector('.flag-en').style.opacity = isPt ? '0.35' : '1';
}

document.addEventListener("DOMContentLoaded", function () {
  applyTranslations();
  updateLangBtn();
});
